    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int flag = fCh.showOpenDialog(NewJFrame.this); //открытия диалогового окна
        if (flag != JFileChooser.APPROVE_OPTION) return; // Проверка на успешное открытие файла; 
        //выбранный файл можно получить методом getFile. (см. ниже)
        // Обработка исключений.
        try
        {   String line=null; 
            BufferedReader bufread = new BufferedReader (new FileReader(fCh.getSelectedFile()));
             while(( line=bufread.readLine())!=null) // построчное считывание потока
                    {
                        tree.add(line);//добавление в поле 
                       
                    }
    }//GEN-LAST:event_jButton1ActionPerformed
        catch(IOException e)
       {
            System.out.println("Connection Failed");
		e.printStackTrace();
		return;
     }
    } 
